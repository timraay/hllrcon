class Model(BaseModel):
    model_config = ConfigDict(
        alias_generator=to_pascal,
        validate_by_name=True,
    )


class Resolvable(ABC, Generic[T]):
    @abstractmethod
    def resolve(self) -> T: ...


class Resolver(Model, Resolvable[T], Generic[T]):
    _resolved_value: T | None = None

    @abstractmethod
    def _resolve_value(self) -> T: ...

    @overload
    def resolve(self) -> T: ...
    @overload
    def resolve(self, validation_model_type: type[ModelT]) -> ModelT: ...

    def resolve(self, validation_model_type: type[ModelT] | None = None) -> T | ModelT:
        if self._resolved_value is None:
            self._resolved_value = self._resolve_value()
        if validation_model_type is not None:
            return validation_model_type.model_validate(self._resolved_value)
        return self._resolved_value


@cache
def read_file(path: Path) -> list[Any]:
    content = path.read_text(encoding="utf-8")
    json_content = json.loads(content)
    if not isinstance(json_content, list):
        msg = f"Expected an array in the JSON file: {path}"
        raise TypeError(msg)
    return json_content


def merge_dicts(base: dict[str, Any], override: dict[str, Any]) -> dict[str, Any]:
    merged = base.copy()
    for key, value in override.items():
        if key in merged and isinstance(merged[key], dict) and isinstance(value, dict):
            merged[key] = merge_dicts(merged[key], value)
        else:
            merged[key] = value
    return merged


class ObjectReference(Resolver[DictT]):
    object_path: str

    def _resolve_value(self) -> DictT:
        match = RE_OBJECT_PATH.match(self.object_path)
        if not match:
            msg = f"Invalid object path: {self.object_path}"
            raise ValueError(msg)
        groupdict = match.groupdict()
        path = HLL_METADATA_PATH / (str(groupdict["path"]) + ".json")
        index = int(groupdict["index"])
        json_content = read_file(path)
        return json_content[index]


class AssetReference(Resolver[DictT]):
    asset_path_name: str
    sub_path_string: str

    def _resolve_value(self) -> DictT:
        match = RE_ASSET_PATH.match(self.asset_path_name)
        if not match:
            msg = f"Invalid asset path: {self.asset_path_name}"
            raise ValueError(msg)
        groupdict = match.groupdict()
        path = HLL_METADATA_PATH / ("HLL/Content/" + str(groupdict["path"]) + ".json")
        json_content = read_file(path)
        return next(obj for obj in json_content if obj.get("Name") == groupdict["name"])


class DataTableEntry(Model):
    data_table: ObjectReference
    row_name: str

    def get(self, model_type: type[ModelT]) -> ModelT:
        data_table = self.data_table.resolve(DataTable[model_type])  # type: ignore[valid-type] # ty:ignore[invalid-type-form]
        row = data_table.rows.get(self.row_name)
        if not row:
            msg = (
                f"Row '{self.row_name}' not found in data table"
                f" '{self.data_table.object_path}'."
            )
            raise ValueError(msg)
        return row


class Object(Model, Generic[ModelT]):
    type: str
    name: str
    properties: ModelT

    @model_validator(mode="before")
    @classmethod
    def inherit_template_properties(cls, data: Any) -> Any:  # noqa: ANN401
        if isinstance(data, dict):
            template_ref = data.get("Template")
            if template_ref is not None:
                template = ObjectReference.model_validate(template_ref).resolve(
                    Object[Any],
                )
                merged_properties = merge_dicts(
                    template.properties,
                    data.get("Properties", {}),
                )
                data["Properties"] = merged_properties
        return data


class Blueprint(Object[Model]):
    class_default_object: ObjectReference

    def get_object(self, object_type: type[ObjectT]) -> ObjectT:
        return self.class_default_object.resolve(object_type)


class DataTable(Object[Model], Generic[ModelT]):
    rows: dict[str, ModelT]


class HLLLoadoutItem(Model):
    display_name: Annotated[
        str,
        Field(validation_alias=AliasPath("DisplayName", "SourceString")),
    ]
    weapon_asset: Annotated[
        AssetReference,
        Field(validation_alias="Weapon", exclude=True),
    ]

    @cached_property
    def weapon(self) -> "HLLWeapon":
        bp = self.weapon_asset.resolve(Blueprint)
        return bp.get_object(Object[HLLWeapon]).properties


