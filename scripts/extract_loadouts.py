def autogenerate_loadouts(loadouts: HLLTeamLoadouts, faction: Faction) -> str:
    content = ""
    for role, loadouts_for_role in loadouts.items():
        for loadout in loadouts_for_role:
            if role is HLLRole.OPERATOR:
                role_name = "OPERATOR"
            elif role is HLLRole.GUNNER:
                role_name = "GUNNER"
            else:
                role_name = role.pretty_name.replace(" ", "_").replace("-", "_").upper()
            loadout_id = (
                f"{faction.short_name}_{role_name}"
                f"_{loadout.display_name.replace(' ', '_').upper()}"
            )

            items = ""
            for item in loadout.loadout_items:
                items += AUTOGENERATED_CONTENT_LOADOUT_ITEM_TEMPLATE.format(item=item)

            content += AUTOGENERATED_CONTENT_LOADOUT_TEMPLATE.format(
                loadout_id=loadout_id,
                faction=faction.short_name,
                role=role_name,
                loadout=loadout,
                items=items,
            )
    return content


def main() -> None:
    if not os.getenv("HLL_METADATA_PATH"):
        msg = "The HLL_METADATA_PATH environment variable is not set."
        raise RuntimeError(msg)

    content_to_add = ""

    for location in LOADOUT_LOCATIONS:
        loadouts = location.read()
        content_to_add += autogenerate_loadouts(loadouts, location.faction)

    output_path = AUTOGENERATED_CONTENT_OUTPUT_PATH
    content = output_path.read_text(encoding="utf-8")

    # Update file contents
    content = content.split(AUTOGENERATED_CONTENT_START_MARKER)[0]
    content += AUTOGENERATED_CONTENT_START_MARKER + "\n"
    content += content_to_add
    output_path.write_text(content, encoding="utf-8")


if __name__ == "__main__":
    main()
